# ============================================================
# 라이브러리 불러오기
# - datetime: 날짜 계산을 위한 기본 내장 라이브러리
# - FinanceDataReader: 주식 데이터를 쉽게 불러오는 라이브러리
# - matplotlib: 차트(그래프)를 그리는 라이브러리
# ============================================================
from datetime import datetime, timedelta
import FinanceDataReader as fdr
import matplotlib.pyplot as plt

# ============================================================
# 한글 폰트 설정
# - matplotlib 기본 폰트는 한글을 지원하지 않아 깨짐
# - 윈도우 기본 폰트인 'Malgun Gothic'으로 변경
# - unicode_minus: 마이너스(-) 기호가 깨지는 현상 방지
# ============================================================
plt.rcParams['font.family'] = 'Malgun Gothic'
plt.rcParams['axes.unicode_minus'] = False

# ============================================================
# 날짜 설정 (오늘 기준 2년치)
# - datetime.today(): 오늘 날짜를 가져옴
# - timedelta(days=365*2): 2년(730일) 전 날짜 계산
# - strftime('%Y-%m-%d'): 날짜를 '2024-01-01' 형식 문자열로 변환
# ============================================================
end = datetime.today()
start = end - timedelta(days=365*2)

# ============================================================
# 주식 데이터 불러오기
# - '005930': 삼성전자 종목코드
# - start~end 기간의 일별 OHLCV 데이터를 DataFrame으로 반환
# - OHLCV: Open(시가), High(고가), Low(저가), Close(종가), Volume(거래량)
# ============================================================
df = fdr.DataReader('005930', start.strftime('%Y-%m-%d'), end.strftime('%Y-%m-%d'))

# ============================================================
# 이동평균 계산
# - rolling(20): 20일 이동 윈도우 설정
# - .mean(): 해당 윈도우 구간의 평균값 계산
# - 결과는 df에 새 컬럼으로 추가됨
# - 예: MA20은 오늘 포함 최근 20일 종가의 평균
# ============================================================
df['MA20'] = df['Close'].rolling(20).mean()
df['MA60'] = df['Close'].rolling(60).mean()

# ============================================================
# 매매 신호 생성
# - df['Signal'] = 0: 모든 날을 기본값 0(중립)으로 초기화
# - MA20 > MA60 인 날을 1(매수 구간)로 표시
# - 골든크로스: 단기선(MA20)이 장기선(MA60) 위로 올라온 상태
# - 데드크로스: 단기선이 장기선 아래로 내려간 상태 (Signal = 0)
# ============================================================
df['Signal'] = 0
df.loc[df['MA20'] > df['MA60'], 'Signal'] = 1

# ============================================================
# 크로스 시점 추출
# - .diff(): 현재값 - 이전값 계산
# - Signal이 0→1로 바뀐 날 = 골든크로스 (diff == 1)
# - Signal이 1→0으로 바뀐 날 = 데드크로스 (diff == -1)
# ============================================================
golden = df[df['Signal'].diff() == 1]    # 골든크로스: 매수 시점
dead   = df[df['Signal'].diff() == -1]   # 데드크로스: 매도 시점

# ============================================================
# 첫 번째 차트: 주가 + 이동평균 + 매매 신호
# ============================================================
plt.figure(figsize=(14, 6))  # 가로 14인치, 세로 6인치 크기

plt.plot(df['Close'], label='종가',  color='gray',   linewidth=1)   # 종가선
plt.plot(df['MA20'],  label='MA20',  color='blue',   linewidth=1)   # 20일 이동평균선
plt.plot(df['MA60'],  label='MA60',  color='orange', linewidth=1)   # 60일 이동평균선

# scatter: 점(▲▼)으로 매수/매도 시점 표시
# marker='^': 위쪽 삼각형(▲), 'v': 아래쪽 삼각형(▼)
# s=100: 점 크기
plt.scatter(golden.index, golden['Close'], marker='^', color='red',  s=100, label='골든크로스 (매수)')
plt.scatter(dead.index,   dead['Close'],   marker='v', color='blue', s=100, label='데드크로스 (매도)')

plt.title('삼성전자 골든크로스 전략 (20/60)')
plt.legend()       # 범례 표시
plt.tight_layout() # 차트 여백 자동 조정

# ============================================================
# 백테스팅
# - 과거 데이터로 전략을 시뮬레이션해서 수익률을 계산
# - initial_cash: 시작 자본금 1000만원
# - cash: 현재 보유 현금
# - stock: 현재 보유 주식 수
# - portfolio: 날짜별 총 자산 가치를 기록하는 리스트
# ============================================================
initial_cash = 10_000_000  # 1000만원 (언더바는 가독성용 구분자)
cash = initial_cash
stock = 0
portfolio = []

# ============================================================
# 날짜별로 순서대로 반복 (시뮬레이션 핵심 루프)
# - range(len(df)): 0부터 데이터 행 수까지 반복
# - iloc[i]: i번째 행의 값을 가져옴
# ============================================================
for i in range(len(df)):
    price = df['Close'].iloc[i]           # 오늘 종가
    signal = df['Signal'].iloc[i]         # 오늘 신호 (0 또는 1)
    prev_signal = df['Signal'].iloc[i-1] if i > 0 else 0  # 어제 신호

    # --------------------------------------------------------
    # 골든크로스 → 매수
    # - 오늘 Signal=1, 어제 Signal=0 이면 신호가 막 발생한 것
    # - cash > 0: 현금이 있을 때만 매수
    # - cash // price: 가진 돈으로 살 수 있는 최대 주식 수 (정수 나눗셈)
    # --------------------------------------------------------
    if signal == 1 and prev_signal == 0 and cash > 0:
        stock = cash // price
        cash -= stock * price
        print(f"{df.index[i].date()} 매수: {stock}주 @ {price:,}원")

    # --------------------------------------------------------
    # 데드크로스 → 매도
    # - 오늘 Signal=0, 어제 Signal=1 이면 신호가 막 꺼진 것
    # - stock > 0: 주식이 있을 때만 매도
    # --------------------------------------------------------
    elif signal == 0 and prev_signal == 1 and stock > 0:
        cash += stock * price
        print(f"{df.index[i].date()} 매도: {stock}주 @ {price:,}원")
        stock = 0

    # --------------------------------------------------------
    # 오늘 총 자산 = 현금 + (보유 주식 수 × 현재가)
    # --------------------------------------------------------
    portfolio.append(cash + stock * price)

# ============================================================
# 마지막 날 보유 주식 강제 청산
# - 백테스팅 종료 시점에 아직 주식을 들고 있으면
#   마지막 날 종가로 팔았다고 가정
# ============================================================
if stock > 0:
    last_price = df['Close'].iloc[-1]
    cash += stock * last_price
    print(f"{df.index[-1].date()} 최종 청산: {stock}주 @ {last_price:,}원")
    stock = 0

# ============================================================
# 결과 출력
# - f-string: 변수를 문자열 안에 삽입하는 방법
# - {:,}: 숫자에 천 단위 콤마 추가 (10000 → 10,000)
# - {:.1f}: 소수점 첫째 자리까지 표시
# ============================================================
final = cash
profit = final - initial_cash
rate = (profit / initial_cash) * 100
print(f"\n초기 자본: {initial_cash:,}원")
print(f"최종 자산: {final:,.0f}원")
print(f"수익금:   {profit:,.0f}원")
print(f"수익률:   {rate:.1f}%")

# ============================================================
# 두 번째 차트: 포트폴리오 가치 변화
# ============================================================
df['Portfolio'] = portfolio  # 날짜별 자산 가치를 df에 추가

plt.figure(figsize=(14, 4))
plt.plot(df.index, df['Portfolio'], color='green', label='포트폴리오 가치')

# axhline: 수평선 그리기 (초기 자본 기준선)
# linestyle='--': 점선
plt.axhline(y=initial_cash, color='red', linestyle='--', label='초기 자본')

plt.title('포트폴리오 가치 변화')
plt.legend()
plt.tight_layout()
plt.show()  # 두 차트를 동시에 표시

